def count_connected_devices(network_range="192.168.1.0/24"):
    nm = nmap.PortScanner()
    nm.scan(hosts=network_range, arguments='-sn')  # Ping Scan
    return len(nm.all_hosts())

wifi_users = count_connected_devices()
print(f"{BLUE}Current devices connect to WIFI: {wifi_users} devices{RESET}")
